# Lista de números
numeros = [5, 3, 9, 1, 7]

# Usando la función sorted()
numeros_ordenados = sorted(numeros)

print("Números ordenados de menor a mayor usando sorted():", numeros_ordenados)

# Usando el método sort() (modifica la lista original)
numeros.sort()

print("Números ordenados de menor a mayor usando sort():", numeros)
